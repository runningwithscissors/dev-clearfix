namespace BoldMinded\Bloqs\Entity;

class BlockGroup
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var string
     */
    private $name;

    /**
     * @var int
     */
    private $order;

    /**
     * These are not properties on BlockDefinition, or used by directly by Bloqs.
     * The Form_validation->setCallbackObject() method sets these properties on
     * the object used in the callback, but PHP 8.2+ does not allow setting dynamic
     * properties, so here we are.
     */
    public $lang;
    public $security;
    public $input;

    /**
     * @return int
     */
    public function getId(): int
    {
        return $this->id;
    }

    /**
     * @param int $id
     * @return $this
     */
    public function setId(int $id): BlockGroup
    {
        $this->id = $id;

        return $this;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @param string $name
     * @return $this
     */
    public function setName(string $name): BlockGroup
    {
        $this->name = $name;

        return $this;
    }

    /**
     * @return int
     */
    public function getOrder(): int
    {
        return $this->order;
    }

    /**
     * @param int $order
     * @return $this
     */
    public function setOrder(int $order): BlockGroup
    {
        $this->order = $order;

        return $this;
    }
}
